package ie.nln.softwaretester.basics;

public class PreAndPost {

	public static void main(String[] args) {
		int num1 = 0;
		
		System.out.println(num1);
		
		num1++;
		
		System.out.println(num1);
		
		++num1;
		
		System.out.println(num1);
		
		
		System.out.println(num1++);
		
		System.out.println(num1);
		
		System.out.println(++num1);
	}
}
